# По введенным пользователем координатам двух точек вывести уравнение прямой вида y = kx + b,
# проходящей через эти точки.
# b=y0-k*x0
# y1=k*x1+y0-k*x0
# k*(x1-x0)=y1-y0
# k=(y1-y0)/(x1-x0)
# b=y0-(y1-y0)*x0/(x1-x0)
x0 = int(input("Введите X0: "))
y0 = int(input("Введите Y0: "))
x1 = int(input("Введите X1: "))
y1 = int(input("Введите Y1: "))
k = (y1 - y0) / (x1 - x0)
b = y0 - (y1 - y0) * x0 / (x1 - x0)
sign = "+"
if b < 0:
    sign = "-"
print(f"y={k}*x{sign}{abs(b)}")
